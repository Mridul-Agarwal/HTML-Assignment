<?php
//all uppercase letters
print(strtoupper("my name is mridul agarwal."))."\n";
//all lowercase letters
print(strtolower("my name is mridul agarwal"))."\n";
// make a string's first character uppercase
print(ucfirst("my name is mridul agarwal."))."\n";
// make a string's first character of all the words uppercase
print(ucwords("my name is mridul agarwal."))."\n";